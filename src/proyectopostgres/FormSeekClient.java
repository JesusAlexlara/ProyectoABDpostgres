    public void openConnection(String u, String ps)
    {
        if (connection != null)
            return;
        
        String url = "jdbc:postgresql://localhost:5432/Autos";
        try
        {
            Class.forName("org.postgresql.Driver");
            connection = DriverManager.getConnection(url, u, ps);
        } catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
    public void closeConnection()
    {
        try
        {
            if(connection!=null)
            {
                connection.close();
                connection = null;      
            }
        }catch(Exception e)
        {
            System.out.println(e.getMessage());
        }
    }
    
    private void cargaJTable(String search)
    {
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsm;
        DefaultTableModel dtm;
        String query = "";
        try{
            if(search.equals(""))
                query = "SELECT * FROM \"cliente\"";
            else
                query = "SELECT * FROM \"cliente\" WHERE nombre LIKE '" + search + "%'";
            ps = connection.prepareStatement(query);
            rs = ps.executeQuery();
            rsm = rs.getMetaData();
            ArrayList<Object[]> data = new ArrayList<>();
           
            while(rs.next())
            {
                Object[] rows = new Object[rsm.getColumnCount()];
                for(int i = 0; i < rows.length; i ++)
                {
                    rows[i] = rs.getObject(i+1);
                }
                data.add(rows);
            }
           
            dtm = (DefaultTableModel)this.jTable1.getModel();
            dtm.setRowCount(0);
            for(int i = 0; i < data.size(); i ++)
            {
                dtm.addRow(data.get(i));
            }
            rs.close();
            ps.close();
           
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane,e.getMessage());
        }
    }

